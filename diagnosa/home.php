<h2>Penyakit Kulit Akibat Jamur</h2>
<p>Penyakit jamur kulit mudah sekali menyerang masyarakat yang tinggal di daerah tropis dimana kelembaban udaranya cukup tinggi. Jamur kulit biasanya berkembang biak pada bagian kulit lembab dan tersembunyi seperti di selangkangan, ketiak, di sela jemari kaki, lipatan kulit lengan, lipatan kulit daun telinga dan kuku jari tangan maupun kaki. Bagian  bagian tubuh tadi adalah daerah lembab dan sering kering apabila sehabis mandi dan jamur sangat menyukai tempat itu. Tempat penyebaran jamur kulit biasanya di tempat - tempat yang sering kita gunakan untuk beraktivitas seperti kamar mandi, salon, mobil, helm dan tempat lainnya.</p>
<td></td>

<p>Jamur kulit dapat menular melalui persinggungan kulit, udara, dan bisa melalui media apapun yang tidak steril. Jamur kulit dapat menular melalui bantal, pakaian atau handuk yang dipakai bergantian dengan penderita jamur kulit. Jadi jamur kulit dapat ditularkan melalui kontak langsung maupun tidak langsung dengan penderita jamur kulit.</p>

<p>Secara umum memang penyakit jamur di kulit berlangsung tahunan terutama untuk jenis jamur yang dapat beradaptasi. Bila diobati, akan kambuh lagi, dan sepertinya sulit untuk disembuhkan. Namun sebenarnya tidak harus demikian. Hal ini mungkin terjadi karena jamur kulit tidak disembuhkan secara tuntas. Kemungkinan kedua mungkin salah memilih obat anti jamur. Karena itulah, bila terinfeksi jamur, obati secara tuntas dengan obat yang benar. Kemungkinan lain adalah karena sumber infeksi jamur masih ada dan belum dihilangkan sehingga dapat terjadi infeksi ulangan.</p>

